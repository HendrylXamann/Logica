#Segue uma demonstração básica de operações aritméticas no python visando demonstrar meus conhecimentos no tema;
#Divisão inteira:
#Versão extensa/arcaica
di1 = 27
di2 = 2
divi = di1 // di2
print(divi)
#Versão otimizada/prática
di1, di2  = 27, 2
print(di1 // di2)
print("-------Fim Inteira, início real ------")
#Divisão real:
#Versão extensa/arcaica
di1 = 27
di2 = 2
divi = di1 / di2
print(divi)
#Versão otimizada/prática
di1, di2  = 27, 2
print(di1 / di2)
print("-------Fim real, início resto ------")
#Resto de divisão:
#Versão extensa/arcaica
di1 = 27
di2 = 2
divi = di1 % di2
print(divi)
#Versão otimizada/prática
di1, di2  = 27, 2
print(di1 % di2)