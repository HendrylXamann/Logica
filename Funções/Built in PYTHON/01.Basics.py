#Aqui é um pequeno trecho de código para mostrar a função round que basicamente serve para arendondar valores

# Arredonda para o número inteiro mais próximo
v1 = 4.6
arredondado1 = round(v1)
print(arredondado1)  

# Arredonda com parametro definido, após casa decima
pi= round(3.14159265358979323846, 2)
print(pi)

# Arredonda número negativo
num3 = -2.8
arredondado3 = round(num3)
print(arredondado3)  # Saída: -3